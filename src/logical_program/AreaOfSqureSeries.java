package logical_program;

public class AreaOfSqureSeries {

	public static void main(String[] args) 
	{
		int a=10;
		
		for(int i=1; i<=a; i++)
		{
			System.out.println(" "+(i*i));
		}
		
		System.out.println("=============");
		
		// cube root of series
		
		int x=15;
		
		for(int j=1; j<=x; j++)
		{
			System.out.println(j*j*j);
		}
			
	}

}
