package logical_program;

public class EvenNoSeries {

	public static void main(String[] args) 
	{
		int n=8;
		
		for(int i=1; i<=n; i++)
		{
			System.out.println(2*i);
		}
		System.out.println("==============");
		
		int a=8;
		
		for(int l=1; l<=a; l++)
		{
			System.out.println(2*l-1);
		}
		
	}

}
