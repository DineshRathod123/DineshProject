package Loops;

public class DoWhileLoopStudy {

	public static void main(String[] args) 
	{
		// i want print JHANVI 10 times
		
		int a=1;
		do
		{
			System.out.println("JHANVI");
			a++;
			
		}
		while(a<=10);
		
		
		// i want to print 15 table in reverse order
		System.out.println("=============");
		
		int b=150;
		do
		{
			System.out.println(b);
			b=b-15;
			
		}
		while(b>=15);
		
	}
	
	

}
