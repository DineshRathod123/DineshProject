package Loops;

public class WhileLoopStudy {

	public static void main(String[] args) 
	{
		// print 5's table
		
		int a=5;
		while(a<=50)
		{
			System.out.println(a);
			
			a=a+5;
			
		}
		
		// print 8 table in reverse order
		System.out.println("============");
		
		int b=80;
		while(b>=8)
		{
			System.out.println(b);
			
			b=b-8;
			
		}

	}

}
