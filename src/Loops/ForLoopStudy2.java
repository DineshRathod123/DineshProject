package Loops;

public class ForLoopStudy2 {

	public static void main(String[] args) 
	{
		// i want print 6's table 
		
		for(int a=6; a<=60; a=a+6)
		{
			System.out.println(a);
		}
		
		// i want print 6's table in decrement order
		
		for(int a=60; a>=6; a=a-6)
		{
			System.out.println(a);
		}
		
		// i want to print prasad 5 times
		
		for(int a=9; a<10; a++)
		{
			System.out.println(" i love u prasad");
		}
		

	}

}
