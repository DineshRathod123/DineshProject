package Loops;

public class ForLoop_Study {

	public static void main(String[] args) 
	{
		// i want to print number 0-10 in increment by 1
		
		for(int a=0; a<=10; a++)
		{
			System.out.println(a);
		}
		
		// i want to print number 10-0 by decrement by 1
		
		for(int a=10; a>=0; a--)
		{
			System.out.println(a);
		}

	}

}
